# Користувач вводить з клавіатури число, яке складається із чотирьох цифр, а
# ви маєте знайти їх добуток. Наприклад, якщо з клавіатури введено 1324, то
# результат добутку буде
# 1*3*2*4 = 24.

num = int(input("Enter  number -"))
num1 = num//1000
num2 = (num//100)%10
num3 =(num//10)%10
num4 = num%10
print(f" {num1}*{num2}*{num3}*{num4} = {num1*num2*num3*num4} ")
